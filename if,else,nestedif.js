// Deklarasi variabel
const nilai = 10;

// Percabangan if
if (nilai > 0) {
  // Blok kode jika nilai lebih dari 0
  console.log("Nilai lebih dari 0");
} else {
  // Blok kode jika nilai kurang dari atau sama dengan 0
  console.log("Nilai kurang dari atau sama dengan 0");
}

// Percabangan nested if
if (nilai > 10) {
  // Blok kode jika nilai lebih dari 10
  console.log("Nilai lebih dari 10");
} else if (nilai > 5) {
  // Blok kode jika nilai lebih dari 5 dan kurang dari atau sama dengan 10
  console.log("Nilai lebih dari 5 dan kurang dari atau sama dengan 10");
} else {
  // Blok kode jika nilai kurang dari atau sama dengan 5
  console.log("Nilai kurang dari atau sama dengan 5");
}
